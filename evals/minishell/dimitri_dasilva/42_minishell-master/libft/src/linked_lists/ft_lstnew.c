#include "libft.h"

t_list	*ft_lstnew(void *data)
{
	t_list	*new_node;

	new_node = malloc(sizeof(t_list));
	if (!new_node)
		return (0);
	new_node->data = data;
	new_node->next = 0;
	return (new_node);
}
