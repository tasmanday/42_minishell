#include <unistd.h>

int	ft_n_putstr(char *str)
{
	int	n;

	if (!str)
		return (ft_n_putstr("(null)"));
	n = 0;
	while (*str)
	{
		write(1, str, 1);
		str++;
		n++;
	}
	return (n);
}
